
[System.Serializable]
public class Einstellungen {

    public float MusikLautstärke;
    public float SoundLautstärke;


    public Einstellungen(float MusikLautstärke, float SoundLautstärke)
    {
        this.MusikLautstärke = MusikLautstärke;
        this.SoundLautstärke = SoundLautstärke;

    }


}
